package DecisionMakingStatements;

public class SwitchExmaple {

	public static void main(String[] args) {
		int number = 2;
		
		switch(number) {
		case 1: 
			System.out.println("Chaithu is very pretty Girl");
			break;
			
		case 2: 
		System.out.println("Raghavendra is a good mentor");
		break;
		
		case 3: 
			System.out.println("3");
			break;
	}
	}
	}


